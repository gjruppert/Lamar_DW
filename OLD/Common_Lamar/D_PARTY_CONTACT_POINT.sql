CREATE TABLE svo.D_PARTY_CONTACT_POINT
(
    PARTY_CONTACT_POINT_SK  BIGINT IDENTITY(1,1) NOT NULL,

    CONTACT_POINT_ID        BIGINT        NOT NULL,  -- bzo.AR_PartyContactPointExtractPVO.ContactPointId
    OWNER_TABLE_ID          BIGINT        NOT NULL,  -- points to PARTY_ID in this PVO set
    CONTACT_POINT_TYPE      VARCHAR(30)   NOT NULL,
    CONTACT_POINT_PURPOSE   VARCHAR(30)   NULL,
    PHONE_NUMBER            VARCHAR(40)   NULL,
    PRIMARY_FLAG            VARCHAR(1)    NULL,

    LAST_UPDATE_DATE        DATE          NOT NULL,
    LAST_UPDATED_BY         VARCHAR(64)   NOT NULL,
    LAST_UPDATE_LOGIN       VARCHAR(32)   NULL,

    BZ_LOAD_DATE            DATE          NOT NULL,
    SV_LOAD_DATE            DATE          NOT NULL
        CONSTRAINT DF_D_PARTY_CONTACT_POINT_SV_LOAD_DATE
        DEFAULT (CAST(GETDATE() AS DATE)),

    CONSTRAINT PK_D_PARTY_CONTACT_POINT PRIMARY KEY CLUSTERED (PARTY_CONTACT_POINT_SK)
        ON FG_SilverDim,

    CONSTRAINT UQ_D_PARTY_CONTACT_POINT UNIQUE (CONTACT_POINT_ID)
        ON FG_SilverDim
)
ON FG_SilverDim;
GO

