INSERT INTO svo.D_PARTY_CONTACT_POINT
(
    CONTACT_POINT_ID,
    OWNER_TABLE_ID,
    CONTACT_POINT_TYPE,
    CONTACT_POINT_PURPOSE,
    PHONE_NUMBER,
    PRIMARY_FLAG,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_LOGIN,
    BZ_LOAD_DATE
)
SELECT
    C.ContactPointId,
    C.OwnerTableId,
    C.ContactPointType,
    C.ContactPointPurpose,
    C.PhoneNumber,
    C.PrimaryFlag,
    C.LastUpdateDate,
    C.LastUpdatedBy,
    C.LastUpdateLogin,
    COALESCE(CAST(C.AddDateTime AS DATE), CAST(GETDATE() AS DATE))
FROM    bzo.AR_PartyContactPointExtractPVO C
WHERE   NOT EXISTS
(
    SELECT 1
    FROM   svo.D_PARTY_CONTACT_POINT D
    WHERE  D.CONTACT_POINT_ID = C.ContactPointId
);
